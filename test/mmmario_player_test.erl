%%--------------------------------------------------------------------
%% @doc
%% テストジェネレータ関数
%% @end
%%--------------------------------------------------------------------
player_test_() ->
  Ins = [
  ],
  {foreach, fun setup_misc/0, fun cleanup_misc/1, Ins}.

%%--------------------------------------------------------------------
%% @doc
%% セットアップ関数
%% @end
%%--------------------------------------------------------------------
setup_misc() ->
  mmmario_room_sup:start_link(),
  {ok, SPid} = mmmario_room_server:start_link(),
  SPid.

%%--------------------------------------------------------------------
%% @doc
%% クリーンアップ関数
%% @end
%%--------------------------------------------------------------------
cleanup_misc(SPid) ->
  exit(SPid, normal).
